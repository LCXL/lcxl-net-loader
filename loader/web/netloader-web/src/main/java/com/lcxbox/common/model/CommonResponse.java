/**
 * 通用响应类
 * @author lcxl
 *
 */
public class CommonResponse {
	private Integer code;
	private Integer status;
	
	public Integer getCode() {
		return code;
	}
	public void setCode(Integer code) {
		this.code = code;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	
	
}
